
friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"]
friends[1] = "Mike"

print(friends)
print(friends[0]) # prints only index 0, first element
print(friends[-1]) # prints last element
print(friends[1:]) # prints from index 1 to last element
print(friends[1:3]) # prints from index 1 up to, but not including index 3
